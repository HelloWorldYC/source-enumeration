% 自定义配色1，在精不在多

clear;
clc;
close all;

rgbTriplet = cell(6, 1);
rgbTriplet{1} = 0.01*round(100*[252, 41, 30;...
    250, 200, 205;...
    219, 249, 244;...
    054, 195, 201;...
    000, 070, 222]/255);
rgbTriplet{2} = 0.01*round(100*[252, 170, 103;...
    255 255 199;...
    084 134 135;...
    071 051 053;...
    189 030 030]/255);
rgbTriplet{3} = 0.01*round(100*[092 158 173;...
    210 204 161;...
    206 190 190;...
    237 177 131;...
    239 111 108]/255);
rgbTriplet{4} = 0.01*round(100*[062 043 109;...
    240 100 073;...
    255 170 050;...
    048 151 164;...
    005 080 091]/255);
rgbTriplet{5} = 0.01*round(100*[246 083 020;...
    124 187 000;...
    000 161 241;...
    255 187 000]/255);
rgbTriplet{6} = [.00 .45 .74;...
    .85 .33 .10;...
    .93 .69 .13;...
    .49 .18 .56];

figure(1);
W = 0.4;
H = 0.24;
L1 = 0.04; L2 = L1 + 0.5;
B1 = 0.02; B2 = 0.33 + B1; B3 = 0.66 + B1;
ax(1) = axes('position', [L1, B3, W, H]);
ax(2) = axes('position', [L1, B2, W, H]);
ax(3) = axes('position', [L1, B1, W, H]);
ax(4) = axes('position', [L2, B3, W, H]);
ax(5) = axes('position', [L2, B2, W, H]);
ax(6) = axes('position', [L2, B1, W, H]);
set(gcf, 'unit', 'centimeters', 'position', [2 2 14.64 16])

for ifigure = 1:6
    axes(ax(ifigure))
    len = length(rgbTriplet{ifigure});
    plot(1, 1/len,...
        'linestyle', 'none',...
        'marker', 'square',...
        'markerfacecolor', rgbTriplet{ifigure}(1, :),...
        'markeredgecolor', rgbTriplet{ifigure}(1, :),...
        'markersize', 30)
    hold on;
    for irgb = 1:3
        str = handle_rgb(rgbTriplet{ifigure}(1, irgb));
        text(1+0.25, (7-2*irgb)/3/len, str,...
            'FontName', 'Times New Roman',...
            'FontSize', 9)
    end
    for icolor = 2:len
        plot(icolor, icolor/len,...
            'linestyle', 'none',...
            'marker', 'square',...
            'markerfacecolor', rgbTriplet{ifigure}(icolor, :),...
            'markeredgecolor', rgbTriplet{ifigure}(icolor, :),...
            'markersize', 30)
        for irgb = 1:3
            str = handle_rgb(rgbTriplet{ifigure}(icolor, irgb));
            text(icolor+0.25, ((7-2*irgb)/3+icolor-1)/len, str,...
                'FontName', 'Times New Roman',...
                'FontSize', 9)
        end
    end
    hold off;
    ax(ifigure).YLim = [0, 1];
    title(['RGB Triplet', '_', num2str(ifigure)]);
    ax(ifigure).Box ='on';
    ax(ifigure).XAxis.Visible = 'off';
    ax(ifigure).YAxis.Visible = 'off';
    set(ax(ifigure), 'FontSize', 10.5, 'FontName', 'Times New Roman');
end
print(gcf, '28_discrete_colors_recommended_in_matlab.png', '-dpng', '-r600')


function str = handle_rgb(rgbTriplet)
str = num2str(rgbTriplet);
str = str(2:end);
if isempty(str)
    str = '.00';
else
    if length(str) == 2
        str = [str, '0'];
    end
end
end